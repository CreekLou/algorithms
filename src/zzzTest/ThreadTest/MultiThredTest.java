package zzzTest.ThreadTest;

class MultiThreadPrinter implements Runnable {
    private String name;
    private Object prev;
    private Object self;

    MultiThreadPrinter(String name, Object prev, Object self) {
        this.name = name;
        this.prev = prev;
        this.self = self;
    }

    @Override
    public void run() {
        int count = 10;
        while (count > 0) {
            synchronized (prev) {
                synchronized (self) {
                    System.out.println(name);
                    count--;
                    self.notify();
                }
                try {
                    prev.wait();
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
            }
        }
    }

    public static void main(String[] args) throws InterruptedException {
        Object a = new Object();
        Object b = new Object();
        Object c = new Object();
        MultiThreadPrinter pa = new MultiThreadPrinter("A", c, a);
        MultiThreadPrinter pb = new MultiThreadPrinter("B", a, b);
        MultiThreadPrinter pc = new MultiThreadPrinter("C", b, c);
        new Thread(pa).start();
        Thread.sleep(100);  //确保按顺序A、B、C执行
        new Thread(pb).start();
        Thread.sleep(100);
        new Thread(pc).start();
        Thread.sleep(100);
    }
}